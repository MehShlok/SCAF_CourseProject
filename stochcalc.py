def stationarity_loss(beta, theta, mu, X):
    """
    L_stat: mean over batch of the variance of OU residuals within each window.
    epsilon_s = Delta_Z_s - theta*(mu - Z_{s-1})   (Eq. 41-42)
    Uses locally-normalised log-prices as a proxy for the spread.
    """
    lp1 = X[:, :, 0]   # (B, W) normalised log s1
    lp2 = X[:, :, 1]   # (B, W) normalised log s2
    Z     = lp1 - beta.unsqueeze(1) * lp2          # (B, W)
    dZ    = Z[:, 1:] - Z[:, :-1]                   # (B, W-1)
    Z_lag = Z[:, :-1]                               # (B, W-1)
    drift = theta.unsqueeze(1) * (mu.unsqueeze(1) - Z_lag)
    eps   = dZ - drift
    return eps.var(dim=1).mean()


def smoothness_loss(beta_curr, beta_prev):
    """L_smooth: penalise rapid changes in hedge ratio (Eq. 43)."""
    return ((beta_curr - beta_prev) ** 2).mean()


