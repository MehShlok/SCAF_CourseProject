def spread_returns(Z):
    """r^Z_t = (Z_t - Z_{t-1}) / |Z_{t-1}|  (Eq. 48)."""
    denom      = np.abs(np.roll(Z, 1))
    denom[0]   = np.nan
    denom      = np.where(denom < 1e-8, 1e-8, denom)
    return np.concatenate([[np.nan], np.diff(Z)]) / denom


def backtest(Z, signal, cost=TRANSACTION_COST, label=""):
    """
    Full backtest with 1-period signal lag (Eq. 58) and transaction costs (Eq. 50).
    Returns daily P&L series + performance metrics.
    """
    r_z      = spread_returns(Z)
    sig      = np.roll(signal, 1);  sig[0] = 0.0    # lookahead guard (Eq. 58)
    r_strat  = sig * r_z                             # Eq. 49
    turnover = np.abs(np.diff(signal, prepend=0.0))
    r_net    = r_strat - cost * turnover             # Eq. 50
    r_net    = np.where(np.isfinite(r_net), r_net, 0.0)

    wealth   = np.cumprod(1 + r_net)                 # Eq. 51

    mu_r  = np.mean(r_net)
    std_r = np.std(r_net) + 1e-10
    SR    = mu_r / std_r * np.sqrt(252)              # Eq. 52

    peak    = np.maximum.accumulate(wealth)
    dd      = (peak - wealth) / (peak + 1e-10)
    MDD     = dd.max()                               # Eq. 53

    ann_ret = wealth[-1] ** (252.0 / len(wealth)) - 1
    calmar  = ann_ret / (MDD + 1e-10)                # Eq. 54

    wins      = r_net > 0
    win_rate  = wins.mean()                          # Eq. 56
    pf        = r_net[wins].sum() / (abs(r_net[~wins].sum()) + 1e-10)  # Eq. 57
    n_trades  = int((turnover > 0).sum())

    return {
        "label":    label,
        "r_net":    r_net,
        "wealth":   wealth,
        "SR":       SR,
        "ann_ret":  ann_ret,
        "MDD":      MDD,
        "calmar":   calmar,
        "win_rate": win_rate,
        "pf":       pf,
        "n_trades": n_trades,
    }


