class PairsLSTM(nn.Module):
    """
    Two-layer stacked LSTM. Maps a window of locally-normalised log-prices
    to OU parameters (beta_t, theta_t, mu_t).

    Input  : (batch, seq_len, 2)   locally normalised log-prices of s1, s2
    Output : beta (B,), theta (B,) >0, mu (B,)
    """
    def __init__(self, input_dim=2, hidden_dim=64, num_layers=2, dropout=0.2):
        super().__init__()
        self.lstm = nn.LSTM(
            input_size=input_dim,
            hidden_size=hidden_dim,
            num_layers=num_layers,
            batch_first=True,
            dropout=dropout if num_layers > 1 else 0.0,
        )
        self.head_beta  = nn.Linear(hidden_dim, 1)
        self.head_theta = nn.Linear(hidden_dim, 1)   # exp() enforces >0 (Eq. 39)
        self.head_mu    = nn.Linear(hidden_dim, 1)

    def forward(self, x):
        out, _ = self.lstm(x)
        h = out[:, -1, :]
        beta  = self.head_beta(h).squeeze(-1)
        theta = torch.exp(self.head_theta(h)).squeeze(-1)
        mu    = self.head_mu(h).squeeze(-1)
        return beta, theta, mu


