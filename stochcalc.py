def normalise_prices(df):
    return df / df.iloc[0]


def ssd_matrix(df):
    """Sum-of-squared deviations between every normalised pair (pipeline step 1)."""
    norm = normalise_prices(df)
    cols = df.columns.tolist()
    records = []
    for a, b in combinations(cols, 2):
        ssd = ((norm[a] - norm[b]) ** 2).sum()
        records.append({"s1": a, "s2": b, "ssd": ssd})
    return pd.DataFrame(records).sort_values("ssd").reset_index(drop=True)


def adf_pval(series):
    """ADF p-value — want p > 0.05 to confirm I(1) (Eq. 12-13)."""
    return adfuller(series, autolag="AIC")[1]


def johansen_hedge_ratio(s1, s2):
    """
    Johansen test. Returns beta* from leading eigenvector,
    trace stat, and 5% critical value (Eq. 22-24).
    """
    df2 = pd.concat([s1, s2], axis=1).dropna()
    res = coint_johansen(df2, det_order=0, k_ar_diff=1)
    trace_stat = res.lr1[0]
    crit_val_5 = res.cvt[0, 1]
    ev         = res.evec[:, 0]
    beta_star  = -ev[0] / ev[1]   # Eq. 24
    return {
        "beta": beta_star,
        "trace_stat": trace_stat,
        "crit_val_5": crit_val_5,
        "cointegrated": trace_stat > crit_val_5,
    }


def fit_ou_params(spread):
    """
    Estimate OU parameters via OLS on DeltaZ = a + b*Z_{t-1} + eps (Eq. 25-28).
    Returns theta, mu, sigma, half_life.
    """
    dz    = spread.diff().dropna()
    z_lag = spread.shift(1).dropna()
    z_lag, dz = z_lag.align(dz, join="inner")
    reg   = OLS(dz, add_constant(z_lag)).fit()
    a, b  = reg.params
    theta     = max(-b, 1e-6)
    mu        = a / theta
    sigma     = reg.resid.std()
    half_life = np.log(2) / theta   # Eq. 10
    return {"theta": theta, "mu": mu, "sigma": sigma, "half_life": half_life}


def hurst_exponent(series, max_lag=100):
    """
    Hurst exponent via log-log regression of lagged std deviations (Eq. 31).
    H < 0.5 -> mean-reverting.
    """
    lags = range(2, min(max_lag, len(series) // 4))
    tau  = [np.std(np.subtract(series[lag:].values, series[:-lag].values))
            for lag in lags]
    tau      = np.array(tau)
    lags_arr = np.array(list(lags))
    valid    = tau > 0
    if valid.sum() < 3:
        return 0.5
    poly = np.polyfit(np.log(lags_arr[valid]), np.log(tau[valid]), 1)
    return float(poly[0])


