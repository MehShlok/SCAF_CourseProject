def generate_signals(zscore):
    """
    Stateful signal generator — Eq. 47.
    +1 = long spread, -1 = short spread, 0 = flat.
    Stop-loss applied at |z| > Z_STOP.
    """
    signal   = np.zeros(len(zscore), dtype=np.float32)
    position = 0.0
    for t in range(len(zscore)):
        z = float(zscore[t])
        if not np.isfinite(z):
            position = 0.0
        else:
            if abs(z) > Z_STOP:
                position = 0.0
            if position != 0 and abs(z) < Z_EXIT:
                position = 0.0
            if position == 0:
                if z < -Z_ENTRY:
                    position = 1.0
                elif z > Z_ENTRY:
                    position = -1.0
        signal[t] = position
    return signal


