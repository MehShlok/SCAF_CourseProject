N_FOLDS    = 3
WFV_EPOCHS = 40   # fewer epochs per fold for speed

fold_size   = len(prices) // (N_FOLDS + 1)
wfv_results = []

print(f"Walk-forward: {N_FOLDS} folds, ~{fold_size} days per fold")

for k in range(N_FOLDS):
    train_end  = fold_size * (k + 1)
    test_end   = fold_size * (k + 2)
    fold_train = prices.iloc[:train_end]
    fold_test  = prices.iloc[train_end:test_end]
    if len(fold_test) < WINDOW + 20:
        continue

    # Re-estimate static OU params for this fold
    joh_k     = johansen_hedge_ratio(fold_train[S1_NAME], fold_train[S2_NAME])
    beta_k    = joh_k["beta"]
    spread_k  = fold_train[S1_NAME] - beta_k * fold_train[S2_NAME]
    ou_k      = fit_ou_params(spread_k)

    # Build windows and re-train LSTM from scratch
    Xk   = make_windows(fold_train, S1_NAME, S2_NAME, WINDOW)
    dl_k = DataLoader(TensorDataset(Xk), batch_size=BATCH_SIZE, shuffle=True, drop_last=True)

    model_k = PairsLSTM(hidden_dim=64, num_layers=2).to(DEVICE)
    opt_k   = AdamW(model_k.parameters(), lr=LR)

    model_k.train()
    prev_bk = None
    for _ in range(WFV_EPOCHS):
        for (Xb,) in dl_k:
            Xb = Xb.to(DEVICE)
            b, t, m = model_k(Xb)
            ls  = stationarity_loss(b, t, m, Xb)
            lsm = (smoothness_loss(b, prev_bk.to(DEVICE))
                   if prev_bk is not None
                   else torch.tensor(0.0, device=DEVICE))
            loss_k = ls + LAMBDA_SMOOTH * lsm
            opt_k.zero_grad()
            loss_k.backward()
            nn.utils.clip_grad_norm_(model_k.parameters(), 1.0)
            opt_k.step()
            prev_bk = b.detach().cpu()

    # Inference on test fold
    full_k  = pd.concat([fold_train.iloc[-WINDOW:], fold_test])
    Xk_inf  = make_windows(full_k, S1_NAME, S2_NAME, WINDOW)
    model_k.eval()
    bk, tk, mk = [], [], []
    with torch.no_grad():
        for i in range(len(Xk_inf)):
            xi = Xk_inf[i:i+1].to(DEVICE)
            b_, t_, m_ = model_k(xi)
            bk.append(b_.item())
            tk.append(t_.item())
            mk.append(m_.item())

    bk = np.array(bk, dtype=np.float32)
    tk = np.array(tk, dtype=np.float32)
    mk = np.array(mk, dtype=np.float32)

    n_test_k = len(fold_test)
    p1k = full_k[S1_NAME].values[WINDOW - 1:].astype(np.float32)
    p2k = full_k[S2_NAME].values[WINDOW - 1:].astype(np.float32)
    Zk_all  = p1k - bk * p2k
    sig_inf_k = np.sqrt(ou_k["sigma"] ** 2 / (2.0 * tk + 1e-8))
    zs_k    = (Zk_all - mk) / (sig_inf_k + 1e-8)

    # Keep test portion only
    Zk_test = Zk_all[-n_test_k:]
    zs_test = zs_k[-n_test_k:]
    sig_k   = generate_signals(zs_test)
    bt_k    = backtest(Zk_test, sig_k, label=f"Fold {k+1}")

    wfv_results.append({
        "fold": k + 1,
        "train_days": train_end,
        "test_days":  n_test_k,
        "SR_oos":     bt_k["SR"],
        "ann_ret":    bt_k["ann_ret"],
        "MDD":        bt_k["MDD"],
    })
    print(f"  Fold {k+1}: OOS Sharpe={bt_k['SR']:.3f}  "
          f"Ann.Ret={bt_k['ann_ret']:.2%}  MDD={bt_k['MDD']:.2%}")

