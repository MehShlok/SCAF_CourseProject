def make_windows(df, s1, s2, window):
    """
    Build (N, window, 2) tensor of locally-normalised log-prices (Remark 3.1).
    Normalise each window: subtract mean, divide by std.
    """
    log1 = np.log(df[s1].values.astype(np.float32))
    log2 = np.log(df[s2].values.astype(np.float32))
    N = len(df) - window + 1
    X = np.empty((N, window, 2), dtype=np.float32)
    for i in range(N):
        w1 = log1[i:i + window]
        w2 = log2[i:i + window]
        X[i, :, 0] = (w1 - w1.mean()) / (w1.std() + 1e-8)
        X[i, :, 1] = (w2 - w2.mean()) / (w2.std() + 1e-8)
    return torch.from_numpy(X)


